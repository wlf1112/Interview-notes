//十进制转换成二进制
console.log(parseInt('1010', 2));

//整数位  当前位的值*2^(n-1)
//小数位  把当前的不停乘2取整
/*
0.1 转化为二进制     0.0001100110011...
0.1*2 =0.2  无整数，取0
0.2*2 =0.4  无整数，取0
0.4*2 =0.8  无整数，取0
0.8*2=1.6   余0.6 
0.6*2=1.2   余0.2
 */

console.log(0.1.toString('2')); //双精度浮点数
//0.0001100110011001100110011001100110011001100110011001101(四舍五入了，比实际的值大)

console.log(0.2.toString('2'));
//0.001100110011001100110011001100110011001100110011001101